public class MiMealModel {
    private String foodName;
    private String quantiy;
    private String unit;
    private String resourceId;
    private int mealId;
    private String mealName;

    public String getMealName() {
        return mealName;
    }

    public void setMealName(String mealName) {
        this.mealName = mealName;
    }

    public MiMealModel(String foodName, String quantiy, String unit, String resourceId, int mealId, String mealName) {
        this.foodName = foodName;
        this.quantiy = quantiy;
        this.unit = unit;
        this.resourceId = resourceId;
        this.mealId = mealId;
        this.mealName = mealName;
    }

    public String getFoodName() {
        return foodName;
    }

    public void setFoodName(String foodName) {
        this.foodName = foodName;
    }

    public String getQuantiy() {
        return quantiy;
    }

    public void setQuantiy(String quantiy) {
        this.quantiy = quantiy;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getResourceId() {
        return resourceId;
    }

    public void setResourceId(String resourceId) {
        this.resourceId = resourceId;
    }

    public int getMealId() {
        return mealId;
    }

    public void setMealId(int mealId) {
        this.mealId = mealId;
    }
}
