package com.patchpets.model.requestModel;

public class HideShowProfileRequest {
    private int userId;
    private String accessToken;
    private int setHideStatus;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public int getSetHideStatus() {
        return setHideStatus;
    }

    public void setSetHideStatus(int setHideStatus) {
        this.setHideStatus = setHideStatus;
    }
}
